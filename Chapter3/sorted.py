# To maintain the original order of a list but present it in a sorted order, you can use the sorted() function. This function effects
# the display but not the actual order of the list like sort() does.

cars = ['bmw', 'audi', 'toyota', 'subaru']

print("Original list:")
print(cars)

print("Sorted list:")
print(sorted(cars))

print("Original list again:")
print(cars)


