birds = ['penguin', 'finch', 'crow']

for bird in birds:
    if bird == 'penguin':
        print(f"A {bird} is able to swim.")
    elif bird == 'finch':
        print(f"A {bird} is able to fly.")
    elif bird == 'crow':
        print(f"A {bird} is also able to see very well.")

print("All of these animals are birds.")